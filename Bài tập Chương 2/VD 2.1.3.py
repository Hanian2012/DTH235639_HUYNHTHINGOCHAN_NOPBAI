#Hàm int() biến đổi số thực, chuỗi thành số nguyên
print(int(3.14))
print(int(3.9999))
print(int(3.0))
print(int(-3.9999))
print(int('12345'))
#Hàm float() biến đổi số nguyên, chuỗi thành số thực
print(float(17))
print(float('123.45'))
#Hàm str() biến đổi số nguyên, số thực thành chuỗi
print(str(17))
print(str('123.45'))